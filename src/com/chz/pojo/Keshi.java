package com.chz.pojo;

public class Keshi {

	private Integer kid;
	private String kname;
	private String kdescribe;
	private String kaddress ;
	public Integer getKid() {
		return kid;
	}
	public void setKid(Integer kid) {
		this.kid = kid;
	}
	public String getKname() {
		return kname;
	}
	public void setKname(String kname) {
		this.kname = kname;
	}
	public String getKdescribe() {
		return kdescribe;
	}
	public void setKdescribe(String kdescribe) {
		this.kdescribe = kdescribe;
	}
	public String getKaddress() {
		return kaddress;
	}
	public void setKaddress(String kaddress) {
		this.kaddress = kaddress;
	}
	
}
